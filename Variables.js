// These are the different ways to make variables. I am showcasing making variables using let, const, and var.
let YOURVARIABLENAME = YOURVARIABLEVALUE; // if your value is a string make sure to sorround it in quotes. 
const YOURVARIABLENAME = YOURVARIABLEVALUE;
// const cannot be changed. 
// the let declaration is only in the scope of its code block
var YOURVARIABLENAME = YOURVARIABLEVALUE;
